// CASE Boolean to integer 1
// RETURNS 1

int main() {
    return !0;
}

// CASE Boolean to integer 2
// RETURNS 0

int main() {
    return !1;
}

// CASE Boolean to integer 3
// RETURNS 0

int main() {
    return 1 == 2;
}

// CASE Boolean to integer 4
// RETURNS 1

int main() {
    return 1 != 2;
}

// CASE No casting needed
// RETURNS 123

int main() {
    return 123;
}
